    errors = 0
    applied = 0
    for line_num, new_content in sorted(replacements.items()):
        idx = line_num - 1
        if idx >= len(lines):
            print(f"Error: Line {line_num} is out of bounds (file has {len(lines)} lines)")
            errors += 1
            continue
            
        old_line = lines[idx].rstrip('\r\n')
        speaker = old_line.split(':')[0] if ':' in old_line else ''
        expected_speaker = new_content.split(':')[0]
        
        if speaker != expected_speaker:
            print(f"Error: Line {line_num} speaker mismatch! Expected {expected_speaker}, got line: {old_line}")
            errors += 1
            continue
            
        ending = '\r\n' if lines[idx].endswith('\r\n') else '\n'
        lines[idx] = new_content + ending
        applied += 1
        
    if errors > 0:
        print(f"Aborting write for {filepath} due to {errors} errors.")
        sys.exit(1)
        
    print(f"Applying {applied} replacements to {filepath}...")
    with open(filepath, 'w', encoding='utf-8', newline='') as f:
        f.writelines(lines)

